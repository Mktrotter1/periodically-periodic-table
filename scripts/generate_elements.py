def write_element_file(z, element):
    """Write a single element JSON file."""
    name = ELEMENTS[z][0].lower()
    filename = f"{z:03d}-{name}.json"
    filepath = ELEMENTS_DIR / filename

    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(element, f, indent=2, ensure_ascii=False)

    return filepath


def main():
    ELEMENTS_DIR.mkdir(parents=True, exist_ok=True)

    print(f"Generating {len(ELEMENTS)} element files...")
    for z in sorted(ELEMENTS.keys()):
        element = build_element(z)
        filepath = write_element_file(z, element)
        name, symbol = ELEMENTS[z]
        print(f"  [{z:3d}] {symbol:3s} {name:15s} -> {filepath.name}")

    print(f"\nDone! Generated {len(ELEMENTS)} element files in {ELEMENTS_DIR}")


if __name__ == "__main__":
    main()
